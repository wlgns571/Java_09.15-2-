package Iot_Java.day05;

public class Comparison_Demo {
	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;
		System.out.println(value1 == value2); // 비교연산자
		System.out.println(value1 != value2); // 같지 않으면...not equal 연산
		System.out.println(value1 > value2);
		System.out.println(value1 < value2);
		System.out.println(value1 <= value2);
		//true, false가 들어갈 수 있는 자리...문장 중에 조건쪽에 들어간다.
	}
}
